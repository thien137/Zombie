# Moving

def check_inputs(surface, player, walls):

	global Alive # Indicator For If Lost Or Not
	global score 
	global collisions # Holds Direction For Which Player Is Right Next To A Wall
	collisions = {'topCollision' : False, 'botCollision' : False, 'rightCollision' : False, 'leftCollision' : False}

	Move = False # Variable Holding If Player Moves

	keys = pygame.key.get_pressed() # List returning keys that have been pressed

	if Alive: # If Not Game Over

		isleftrightcollision(player, walls) # Check If Touching Wall On Left Or Right

		# If Right/Left Arrow Key Pressed, If Player Not At Screen Limit, If Character Not Colliding With Wall On the Right/Left:
		if keys[pygame.K_RIGHT] and player.x < (surface.get_width() - player.depth) and not collisions['rightCollision']:

			player.x += player.vel # Move Player To the Right
			
			Move = True # Player Has Moved
		
		if keys[pygame.K_LEFT] and player.x > 0 and not collisions['leftCollision']:

			player.x -= player.vel # Move Player To The Left

			Move = True # Player Has Moved
		
		# Separate Moving Left/Right and Moving Up/Down To Prevent Bug Where Player Can Clip Into A Wall At A Corner By Timing A Diagonal Move

		istopundercollision(player, walls) # Check If Touching Wall On Top Or Below

		# If Up/Down Arrow Key Pressed, If Player Not At Screen Limit, If Character Not Colliding With Wall On Top/Below:

		if keys[pygame.K_UP] and player.y > 0 and not collisions['topCollision']: 

			player.y -= player.vel # Move Player Up

			Move = True # Player Has Moved
		
		if keys[pygame.K_DOWN] and player.y < (surface.get_height() - player.depth) and not collisions['botCollision']:

			player.y += player.vel # Move Player Down

			Move = True # Player Has Moved

	else: # If Game Over, allow restart button 'r'

		# If 'r' key pressed

		if keys[pygame.K_r]:

			# Keep Picking A Random New Position For the Player Until Find One That Is Not On Top Of A Wall
			while True:

				player.x = random.randint(5, 75) * 10
				player.y = random.randint(5, 75) * 10

				if 1 in [matrix[player.x//10 + j][player.y//10 + i] for i in range(player.depth//10) for j in range(player.width//10)]:
					pass
				else:
					break

			Alive = True # Reset, Game Is Going, Not Game Over

			Move = True # Player Has Moved, Initiate Enemy Movement

			score = 0 # Reset The Score
	
	# If Player Has Moved, Return True; Else, Return False
	if Move:
		return True
	else:
		return False


# Collisions

def isleftrightcollision(player, walls):

	""" Check If Player Is Close To A Wall From The Left Or Right """

	for wall in walls:
		if wall.x - player.x >= 0 and wall.x - player.x <= player.width and (wall.y - player.y > -10 and wall.y - player.y < player.depth) and not collisions['rightCollision']:
			collisions['rightCollision'] = True
		if wall.x - player.x >= -10 and wall.x - player.x <= 10 and (wall.y - player.y > -10 and wall.y - player.y < player.depth) and not collisions['leftCollision']:
			collisions['leftCollision'] = True

def istopundercollision(player, walls):

	""" Check If Player Is Close To A All From Top Or Below """

	for wall in walls:
		if wall.y - player.y >= -10 and wall.y - player.y <= 10 and (wall.x - player.x > -10 and wall.x - player.x < player.width) and not collisions['topCollision']:
			collisions['topCollision'] = True
		if wall.y - player.y >= 0 and wall.y - player.y <= player.depth and (wall.x - player.x > -10 and wall.x - player.x < player.width) and not collisions['botCollision']:
			collisions['botCollision'] = True

def isCollision(player, enemy):

	""" Check If Enemy Has Collided With Player... If So, Game Over """

	if abs(player.x - enemy.x) < player.width and abs(player.y - enemy.y) < player.depth:
		return True

